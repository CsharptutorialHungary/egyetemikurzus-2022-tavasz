namespace KilencedikOra.Shapes;

/// <summary>
/// The kind or classification of a triangle.
/// </summary>
public enum Kind
{
    /// <summary>
    /// A triangle with two equal sides.
    /// </summary>
    Isosceles,

    /// <summary>
    /// A triangle with three equal sides.
    /// </summary>
    Equilateral,

    /// <summary>
    /// A triangle with no equal sides.
    /// </summary>
    Scalene
}